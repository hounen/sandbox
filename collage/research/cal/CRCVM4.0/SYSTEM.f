C==== SYSTEM SETTING ==================================================

      SUBROUTINE SET_SYSTEM

C----------------------------------------------------------------------
      INCLUDE 'DIM_INC.f'
      INCLUDE 'GEM_INC.f'
      IF(INF_NN(1).EQ.0) THEN
        DMASS1=4.D0*DMASS
      ELSE
        DMASS1=DMASS
      ENDIF
      IF(INF_NN(2).EQ.0) THEN
        DMASS2=4.D0*DMASS
      ELSE
        DMASS2=DMASS
      ENDIF
      IF(INF_NN(3).EQ.0) THEN
        DMASS3=4.D0*DMASS
      ELSE
        DMASS3=DMASS
      ENDIF
      ALMASS=DMASS1+DMASS2+DMASS3
C
C REDUCED MASS
C
      DMU1(1)=DMASS2*DMASS3/(DMASS2+DMASS3)
      DMU1(2)=DMASS3*DMASS1/(DMASS3+DMASS1)
      DMU1(3)=DMASS1*DMASS2/(DMASS1+DMASS2)
      DMU2(1)=(DMASS2+DMASS3)*DMASS1/ALMASS
      DMU2(2)=(DMASS3+DMASS1)*DMASS2/ALMASS
      DMU2(3)=(DMASS1+DMASS2)*DMASS3/ALMASS
C
C DIAGONAL
C
      DO I=1,3
        TCO_11(I,I)=1.D0
        TCO_12(I,I)=0.D0
        TCO_21(I,I)=0.D0
        TCO_22(I,I)=1.D0
      ENDDO
C
C OFF-DIAGONAL
C
      TCO_11(1,2)=-DMASS1/(DMASS3+DMASS1)
      TCO_12(1,2)= 1.D0
      TCO_21(1,2)=-ALMASS*DMASS3/((DMASS3+DMASS1)*(DMASS2+DMASS3))
      TCO_22(1,2)=-DMASS2/(DMASS2+DMASS3)

      TCO_11(2,3)=-DMASS2/(DMASS1+DMASS2)
      TCO_12(2,3)= 1.D0
      TCO_21(2,3)=-ALMASS*DMASS1/((DMASS1+DMASS2)*(DMASS3+DMASS1))
      TCO_22(2,3)=-DMASS3/(DMASS3+DMASS1)

      TCO_11(3,1)=-DMASS3/(DMASS2+DMASS3)
      TCO_12(3,1)= 1.D0
      TCO_21(3,1)=-ALMASS*DMASS2/((DMASS2+DMASS3)*(DMASS1+DMASS2))
      TCO_22(3,1)=-DMASS1/(DMASS1+DMASS2)

      TCO_11(2,1)=-DMASS2/(DMASS2+DMASS3)
      TCO_12(2,1)=-1.D0
      TCO_21(2,1)= ALMASS*DMASS3/((DMASS2+DMASS3)*(DMASS3+DMASS1))
      TCO_22(2,1)=-DMASS1/(DMASS3+DMASS1)

      TCO_11(3,2)=-DMASS3/(DMASS3+DMASS1)
      TCO_12(3,2)=-1.D0
      TCO_21(3,2)= ALMASS*DMASS1/((DMASS3+DMASS1)*(DMASS1+DMASS2))
      TCO_22(3,2)=-DMASS2/(DMASS1+DMASS2)

      TCO_11(1,3)=-DMASS1/(DMASS1+DMASS2)
      TCO_12(1,3)=-1.D0
      TCO_21(1,3)= ALMASS*DMASS2/((DMASS1+DMASS2)*(DMASS2+DMASS3))
      TCO_22(1,3)=-DMASS3/(DMASS2+DMASS3)
      RETURN
      END
