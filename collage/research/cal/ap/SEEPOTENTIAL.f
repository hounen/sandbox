      PROGRAM SEEPOTENTIAL
      INCLUDE 'LI_INC.f'
      DIMENSION DKKNN(10000),SBB(10000),CENT(10000),CENL(10000),
     &          DLSFRS(10000),WS(10000),WSLS(10000),DCNTR(10000),
     &          COULB(10000),DDATA(10000),
     &          VCCE(2),RCCE(2),VCCL(3),RCCL(3),VCLS(2),RCLS(2)
      OPEN(UNIT=60,FILE='KKNN.d',STATUS='UNKNOWN')
      OPEN(UNIT=61,FILE='SBB.d',STATUS='UNKNOWN')
      OPEN(UNIT=62,FILE='COULB.d',STATUS='UNKNOWN')

      IME=10000
      DIST=10.D0
      DME=DIST/IME
      DMH=HBC**2.D0/1.6D0/DMASS
      DL=1.D0
      DJ=DL+0.5D0

C CENTRIFUGAL POTENTIAL
      
      DO IR=0,IME
      R=DME*IR
      DMH=HBC**2.D0/1.6D0/DMASS
      DCNTR(IR)=DMH*DL*(DL+1.D0)/R**2.D0
      ENDDO

C FOLDING COULOMB POTENTIAL

      IMEC=1000!EVEN!
      DKAP=DSQRT(0.75D0)*1.4D0
      DMEC=1.D0/DKAP/IMEC
      
      DO IR=0,IME
      R=DME*IR
C      DO I=0,IMEC
C      DDATA(I)=DEXP(-(R*I)**2.D0)
C      ENDDO
C      COULB(IR)=0.D0
C      DO I=0,IMEC-1
C      COULB(IR)=COULB(IR)+0.5D0*(DDATA(I)+DDATA(I+1))*DMEC
C      ENDDO
      COULB(IR)=2.D0*AL*HBC*2.D0/DSQRT(PI)/R!*COULB(IR)
      WRITE(62,*) R,COULB(IR)
      ENDDO
     
C KKNN POTENTIAL

      DO IR=0,IME
      R=DME*IR

      VCCE(1)=-96.3D0
      RCCE(1)= 0.36D0
      VCCE(2)= 77.0D0
      RCCE(2)=  0.9D0

      CENT(IR)=0.D0
      DO I=1,2
      CENT(IR)=CENT(IR)+VCCE(I)*DEXP(-RCCE(I)*R**2.D0)
      ENDDO

      VCCL(1)= 34.0D0
      RCCL(1)= 0.20D0
      VCCL(2)=-85.0D0
      RCCL(2)= 0.53D0
      VCCL(3)= 51.0D0
      RCCL(3)= 2.50D0
      
      CENL(IR)=0.D0
      DO I=1,3
      CENL(IR)=CENL(IR)+VCCL(I)*DEXP(-RCCL(I)*R**2.D0)
      ENDDO
      CENL(IR)=(-1.D0)**DL*CENL(IR)

      VALS=    -8.4D0
      RALS=    0.52D0
      VCLS(1)= -10.D0
      RCLS(1)=0.396D0
      VCLS(2)= 10.0D0
      RCLS(2)= 2.20D0

      DLSFRS(IR)=0.D0
      DO I=1,2
      DLSFRS(IR)=DLSFRS(IR)+VCLS(I)*DEXP(-RCLS(I)*R**2.D0)
      ENDDO
      DLSFRS(IR)=(1.D0+0.3D0*(-1.D0)**(DL-1.D0))*DLSFRS(IR)
      DLSFRS(IR)=VALS*DEXP(-RALS*R**2.D0)+DLSFRS(IR)
      DLS=DJ*(DJ+1.D0)-DL*(DL+1.D0)-0.75D0
      DLSFRS(IR)=DLS*DLSFRS(IR)

      DKKNN(IR)=CENT(IR)+CENL(IR)+DLSFRS(IR)+DCNTR(IR)+COULB(IR)
      WRITE(60,*) R,DKKNN(IR)
      ENDDO

C SBB POTENTIAL
      
      DO JR=0,IME
      R=DME*JR
      WSLS(JR)=40.D0/(1.D0+DEXP((R-1.5D0)/0.35D0))
      ENDDO

      DO IR=0,IME
      R=DME*IR
      WS(IR)=-43.D0/(1.D0+DEXP((R-2.D0)/0.7D0))
      DLS=0.5D0*(DJ*(DJ+1.D0)-DL*(DL+1.D0)-0.75D0)
      WSLS(IR)=1.D0/R*DLS*(WSLS(IR+1)-WSLS(IR))/DME
      SBB(IR)=WS(IR)+WSLS(IR)+DCNTR(IR)+COULB(IR)
      WRITE(61,*) R,SBB(IR)
      ENDDO
      CLOSE(60)
      CLOSE(61)

      STOP
      END PROGRAM SEEPOTENTIAL

      FUNCTION SIMPSON(DATA,IME,DME)
      INCLUDE 'LI_INC.f'
      DIMENSION DATA(0:10000)

      IF(IME/2*2.NE.IME) GOTO 9999
      SIMPSON=DATA(0)-DATA(IME)
      DO 5000 I=1,IME/2
      IO=2*I-1
      IE=2*I
 5000 SIMPSON=SIMPSON+4.D0*DATA(IO)+2.D0*DATA(IE)
      SIMPSON=SIMPSON/3.D0*DME
      RETURN 
